//Consider these values: 5, 10

//Using mathematical functions, find:
//The smallest value,
//The largest value.

#include <iostream>
using namespace std;

int main() {
    int a=5;
    int b=10;

    cout<<"The largest value is: "<<max(a,b)<<endl;
    cout<<"The smallest value is: "<<min(a,b)<<endl;

}